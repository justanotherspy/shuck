func TestDiscoverCreate(t *testing.T) {
	detected := []Detected{
		{Ecosystem: "gomod", Directories: []string{"/"}},
		{Ecosystem: "npm", Directories: []string{"/a", "/b"}},
	}
	d, err := Discover(nil, detected)
	if err != nil {
		t.Fatalf("Discover: %v", err)
	}
	if !d.Created || !d.Changed {
		t.Fatalf("expected created+changed, got %+v", d)
	}
	if len(d.Added) != 2 {
		t.Errorf("added = %v", d.Added)
	}
	// The generated config must round-trip through the strict parser.
	cfg, err := Parse(d.Data)
	if err != nil {
		t.Fatalf("generated config does not parse: %v\n%s", err, d.Data)
	}
	if len(cfg.Updates) != 2 {
		t.Fatalf("updates = %d", len(cfg.Updates))
	}
	// Single-dir uses `directory`, multi-dir uses `directories`.
	body := string(d.Data)
	if !strings.Contains(body, "package-ecosystem: gomod") || !strings.Contains(body, "directories:") {
		t.Errorf("unexpected body:\n%s", body)
	}
	if !strings.Contains(body, "Generated by `shuck dependabot discover`") {
		t.Error("missing header comment")
	}
	// Best-practice fields are present.
	for _, want := range []string{"interval: weekly", "open-pull-requests-limit: 10", "labels:", "groups:", "prefix:"} {
		if !strings.Contains(body, want) {
			t.Errorf("generated config missing %q:\n%s", want, body)
		}
	}
}

