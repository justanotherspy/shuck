func TestDiscoverExtend(t *testing.T) {
	existing := []byte(`version: 2
updates:
  # keep me
  - package-ecosystem: gomod
    directory: /
    schedule:
      interval: weekly
`)
	detected := []Detected{
		{Ecosystem: "gomod", Directories: []string{"/"}},
		{Ecosystem: "docker", Directories: []string{"/"}},
	}
	d, err := Discover(existing, detected)
	if err != nil {
		t.Fatalf("Discover: %v", err)
	}
	if d.Created {
		t.Error("should not be created")
	}
	if !d.Changed || len(d.Added) != 1 || d.Added[0] != "docker" {
		t.Fatalf("expected docker added, got %+v", d)
	}
	body := string(d.Data)
	if !strings.Contains(body, "# keep me") {
		t.Errorf("existing comment lost:\n%s", body)
	}
	if !strings.Contains(body, "package-ecosystem: docker") {
		t.Errorf("docker entry not appended:\n%s", body)
	}
	if _, err := Parse(d.Data); err != nil {
		t.Fatalf("extended config does not parse: %v\n%s", err, body)
	}
}

